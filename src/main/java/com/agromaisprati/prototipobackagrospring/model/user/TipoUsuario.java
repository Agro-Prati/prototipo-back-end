package com.agromaisprati.prototipobackagrospring.model.user;

/**
 * Enum que representa os tipos de usuários no sistema AgroMais
 */
public enum TipoUsuario {
    /**
     * Agricultor - usuário que busca ajuda e consultas
     */
    AGRICULTOR,
    
    /**
     * Agrônomo - profissional especializado em culturas e solos
     */
    AGRONOMO,
    
    /**
     * Veterinário - profissional especializado em saúde animal
     */
    VETERINARIO,
    
    /**
     * Zootecnista - profissional especializado em produção animal
     */
    ZOOTECNISTA,
    
    /**
     * Estudante - estudante de áreas relacionadas ao agronegócio
     */
    ESTUDANTE
}
